# Личностный тест
ans1 = input('Любите ли вы котиков? (да/нет)')
ans2 = input('Умеете ли вы программировать? (да/нет)')
if ans1 == 'да':
    if ans2 == 'да':
        print('Отлично!')
    elif ans2 == 'нет':
        print('Хорошо.')
    else:
        print('Ошибка!')
elif ans1 == 'нет':
    if ans2 == 'да':
        print('Неплохо.')
    elif ans2 == 'нет':
        print('Еще не все потеряно.')
    else:
        print('Ошибка!')
else:
    print('Ошибка!')
