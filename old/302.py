# Банковские проценты
# Вклад в банке составляет x рублей. Ежегодно он увеличивается на p процентов,
# после чего дробная часть от копеек отбрасывается. Определите, через сколько лет вклад
# составит не менее y рублей. В задаче запрещено использовать дробные числа.
x = int(input()) * 100
p = int(input())
y = int(input()) * 100
c = 0
while x < y:
    x += x * p // 100
    c += 1
print(c)
