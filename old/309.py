# Самое частое число в последовательности
# Последовательность состоит из натуральных чисел, причем какое-то из чисел составляет
# более половины от общего числа членов последовательности. Найдите это число.
# Для решения этой задачи запрещено использование массивов и списков.
n = int(input())
ans, count = n, 1
while n != 0:
    if n == ans:
        count += 1
    elif count > 1:
        count -= 1
    else:
        ans = n
        count = 1
    n = int(input())
print(ans)
