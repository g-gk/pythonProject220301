# Количество локальных максимумов
# Элемент последовательности называется строгим локальным максимумом, если он строго
# больше предыдущего и последующего элементов последовательности. Первый и последний
# элемент последовательности не являются локальными максимумами.
a, b, c = 0, 0, int(input())
count = 0
while c != 0:
    if a != 0 and b != 0 and a < b > c:
        count += 1
    a, b, c = b, c, int(input())
print(count)
