# Ход слона
# Шахматный слон ходит по диагонали. Даны две различные клетки шахматной доски, определите,
# может ли слон попасть с первой клетки на вторую одним ходом.
k = int(input())
l = int(input())
m = int(input())
n = int(input())
if abs(k - m) == abs(l - n):
    print('YES')
else:
    print('NO')
