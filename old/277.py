# Ход ладьи
# Шахматная ладья ходит по горизонтали или вертикали. Даны две различные клетки шахматной доски,
# определите, может ли ладья попасть с первой клетки на вторую одним ходом.
k = int(input())
l = int(input())
m = int(input())
n = int(input())
if (k - m) == 0 or (l - n) == 0:
    print('YES')
else:
    print('NO')
